# Minimal 44-byte valid WAV (silent)
$bytes = [byte[]]@(
  0x52,0x49,0x46,0x46,0x24,0x00,0x00,0x00,  # RIFF + size
  0x57,0x41,0x56,0x45,0x66,0x6d,0x74,0x20,  # WAVE fmt 
  0x10,0x00,0x00,0x00,0x01,0x00,0x02,0x00,  # fmt chunk
  0x44,0xac,0x00,0x00,0x10,0xb1,0x02,0x00,  # 44100 Hz stereo
  0x04,0x00,0x10,0x00,0x64,0x61,0x74,0x61,  # data
  0x00,0x00,0x00,0x00
)
$dir = Split-Path -Parent $MyInvocation.MyCommand.Path
[IO.File]::WriteAllBytes("$dir\wall.wav", $bytes)
[IO.File]::WriteAllBytes("$dir\breakout.wav", $bytes)
Write-Host "Created wall.wav and breakout.wav"
